<?php

/**
 * This is a enum of all possible fonts.
 *
 * @link [Docs](https://squidfunk.github.io/mkdocs-material/setup/changing-the-fonts/)
 */
enum Font: int
{
    case ROBOT = 0;
    case ROBOTO_MONO = 1;
    case OPEN_SANS = 2;
    case LATO = 3;
    case MONTSERRAT = 4;
    case RALEWAY = 5;
    case UBUNTU = 6;
}
